package entity;

public class SuppliersEntity {
	private String company_name;
	private String company_street;
	private String company_city;
	private int company_cp;
	/**
	 * @return the company_name
	 */
	public String getCompany_name() {
		return company_name;
	}
	/**
	 * @param company_name the company_name to set
	 */
	public void setCompany_name(String company_name) {
		this.company_name = company_name;
	}
	/**
	 * @return the company_street
	 */
	public String getCompany_street() {
		return company_street;
	}
	/**
	 * @param company_street the company_street to set
	 */
	public void setCompany_street(String company_street) {
		this.company_street = company_street;
	}
	/**
	 * @return the company_city
	 */
	public String getCompany_city() {
		return company_city;
	}
	/**
	 * @param company_city the company_city to set
	 */
	public void setCompany_city(String company_city) {
		this.company_city = company_city;
	}
	/**
	 * @return the company_cp
	 */
	public int getCompany_cp() {
		return company_cp;
	}
	/**
	 * @param company_cp the company_cp to set
	 */
	public void setCompany_cp(int company_cp) {
		this.company_cp = company_cp;
	}
	/**
	 * @return the contact_name
	 */
	public String getContact_name() {
		return contact_name;
	}
	/**
	 * @param contact_name the contact_name to set
	 */
	public void setContact_name(String contact_name) {
		this.contact_name = contact_name;
	}
	/**
	 * @return the contact_firstName
	 */
	public String getContact_firstName() {
		return contact_firstName;
	}
	/**
	 * @param contact_firstName the contact_firstName to set
	 */
	public void setContact_firstName(String contact_firstName) {
		this.contact_firstName = contact_firstName;
	}
	/**
	 * @return the tel_number
	 */
	public String getTel_number() {
		return tel_number;
	}
	/**
	 * @param tel_number the tel_number to set
	 */
	public void setTel_number(String tel_number) {
		this.tel_number = tel_number;
	}
	private String contact_name;
	private String contact_firstName;
	private String tel_number;

}
